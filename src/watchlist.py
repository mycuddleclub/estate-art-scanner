# Keywords in sale descriptions that bump priority score
HIGH_VALUE_DESCRIPTION_SIGNALS = [
    "collection", "collector", "estate of", "artist", "gallery",
    "museum", "catalogue", "provenance", "acquired", "exhibition",
    "framed art", "original", "canvas", "oil painting", "sculpture",
    "contemporary", "modern art", "artwork", "paintings",
    "mid-century", "abstract", "lithograph", "bronze",
    "important", "significant", "curated", "fine art",
]

# Estate sale companies known to handle high-end estates
# Build this list over time from experience
TRUSTED_HIGH_END_COMPANIES = [
    "stefek",
    "leslie hindman",
    "wright",
    "treadway",
    "cowan",
    "hindman",
]


def score_description(description: str) -> int:
    """Return 0-100 score based on description language signals."""
    if not description:
        return 0
    desc_lower = description.lower()
    hits = sum(1 for signal in HIGH_VALUE_DESCRIPTION_SIGNALS if signal in desc_lower)
    return min(100, hits * 12)


def is_trusted_company(org_name: str) -> bool:
    if not org_name:
        return False
    name_lower = org_name.lower()
    return any(company in name_lower for company in TRUSTED_HIGH_END_COMPANIES)
